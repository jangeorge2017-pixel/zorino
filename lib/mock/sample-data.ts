function storeBySlug(slug: string) {
  return MOCK_STORES.find((s) => s.slug === slug);
}

function productToSearchItem(product: Product, storeSlug: string, price: number, originalPrice: number): SearchResultItem {
  const store = storeBySlug(storeSlug);
  const discount = originalPrice > 0 ? Math.round(((originalPrice - price) / originalPrice) * 100) : 0;
  return {
    id: product.id,
    name: product.name,
    imageSrc: product.imageUrl,
    emoji: product.emoji ?? "🛍️",
    price,
    originalPrice,
    discount,
    store: store?.name ?? storeSlug,
    storeSlug,
    rating: product.rating ?? 4.5,
    reviewCount: product.reviewCount,
    inStock: product.inStock,
    category: product.categorySlug ?? "more",
  };
}

